package Lesson1;
//Типы переменных
public class Variables {
    public static void main(String[] args) {
        int a; //Декларация переменной (место в памяти)
        a = 5; //Инизиализация переменной

        int myInt = 5; //Целочисленные
        short myShort = 32; //До 32 тысяч
        long myLong = 233; //Длинные значения

        double myDouble = 2.3; //Плавающая точка
        float myFloat = 2.3f; //Небольшие значения

        char myChar = '@'; //Символы

        boolean myBoolean = true; //Булевые

        byte myByte = 5; //До 8 бит (-128 / +128)

        String myString = "Строка"; //Строки
    }
}
